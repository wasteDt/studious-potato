drop table if exists inspections;
drop table if exists monthly_energy;
drop table if exists daily_energy;
drop table if exists buildings;

create table buildings (
  id integer primary key,
  name text not null,
  code text not null,
  floors integer not null,
  rooms integer not null,
  area integer not null,
  status text not null
);

create table daily_energy (
  id integer primary key,
  building_id integer not null references buildings(id),
  date date not null,
  electricity numeric not null,
  water numeric not null
);

create table monthly_energy (
  id integer primary key,
  building_id integer not null references buildings(id),
  month text not null,
  electricity numeric not null,
  water numeric not null
);

create table inspections (
  id bigint generated by default as identity primary key,
  building_id integer not null references buildings(id),
  building_name text not null,
  type text not null,
  content text not null,
  created_at text not null
);

alter table buildings enable row level security;
alter table daily_energy enable row level security;
alter table monthly_energy enable row level security;
alter table inspections enable row level security;

create policy "public read buildings" on buildings for select using (true);
create policy "public read daily energy" on daily_energy for select using (true);
create policy "public read monthly energy" on monthly_energy for select using (true);
create policy "public read inspections" on inspections for select using (true);
create policy "public insert inspections" on inspections for insert with check (true);

